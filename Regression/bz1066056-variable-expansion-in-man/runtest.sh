# Include Beaker environment
. /usr/bin/rhts-environment.sh || exit 1
. /usr/share/beakerlib/beakerlib.sh || exit 1

PACKAGES=${PACKAGES:-"nginx"}
NGINX_SCL=${NGINX_SCL:-"nginx14"}

rlJournalStart
    rlPhaseStartSetup
        rlAssertRpm --all
        rlRun "rlImport nginx/nginx"
        rlRun "TmpDir=\$(mktemp -d)" 0 "Creating tmp directory"
        rlRun "pushd $TmpDir"
    rlPhaseEnd

    rlPhaseStartTest
        if [ "$nginxCOLLECTION" != 0 ]; then
        rlRun "scl enable $nginxCOLLECTION_NAME 'man nginx > nginx_man'" 0\
            "Getting content of man page"
	else
        rlRun "man nginx > nginx_man" 0   "Getting content of man page"
	fi
        rlAssertNotGrep "%%.*%%" nginx_man || grep "%%.*%%" nginx_man
    rlPhaseEnd

    rlPhaseStartCleanup
        rlRun "popd"
        rlRun "rm -r $TmpDir" 0 "Removing tmp directory"
    rlPhaseEnd
rlJournalPrintText
rlJournalEnd
